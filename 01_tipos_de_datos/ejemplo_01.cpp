#include <iostream>
int main() {
  float num_flotante = 0.9f;
  int num_entero = 9;

  std::cout << "Multiplicacion " << (num_flotante * num_entero) << std::endl;
  std::cout << "Suma " << (num_flotante + num_entero) << std::endl;
  std::cout << "Division " << (num_entero / num_flotante) << std::endl;
  std::cout << "Resta " << (num_flotante -  num_entero) << std::endl;
}
